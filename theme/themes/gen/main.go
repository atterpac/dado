// Command gen reads the hand-edited theme definitions in theme/themes/defs/*.yaml
// and emits a compile-safe Go file (themes_gen.go) registering each theme.
//
// Run via `go generate ./...` from the repo root, or `go run ./gen` from the
// theme/themes directory. The YAML files are the source of truth; the generated
// Go is never edited by hand.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"

	"gopkg.in/yaml.v3"
)

// def mirrors a defs/*.yaml file.
type def struct {
	Name    string            `yaml:"name"`
	Ident   string            `yaml:"ident"`
	Default bool              `yaml:"default"`
	Hidden  bool              `yaml:"hidden"`
	Colors  map[string]string `yaml:"colors"`
}

// fields maps a YAML color key to its theme.ThemeColors Go field name, in the
// order they are emitted. Every theme must define all of them.
var fields = [][2]string{
	{"bg", "Bg"},
	{"bg_light", "BgLight"},
	{"bg_dark", "BgDark"},
	{"fg", "Fg"},
	{"fg_dim", "FgDim"},
	{"fg_muted", "FgMuted"},
	{"accent", "Accent"},
	{"accent_dim", "AccentDim"},
	{"highlight", "Highlight"},
	{"success", "Success"},
	{"warning", "Warning"},
	{"error", "Error"},
	{"info", "Info"},
	{"border", "Border"},
	{"border_focus", "BorderFocus"},
	{"header", "Header"},
	{"menu", "Menu"},
	{"table_header", "TableHeader"},
	{"key", "Key"},
	{"crumb", "Crumb"},
	{"panel_border", "PanelBorder"},
	{"panel_title", "PanelTitle"},
}

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "gen:", err)
		os.Exit(1)
	}
}

func run() error {
	paths, err := filepath.Glob("defs/*.yaml")
	if err != nil {
		return err
	}
	if len(paths) == 0 {
		return fmt.Errorf("no defs/*.yaml found (run from theme/themes)")
	}
	sort.Strings(paths)

	var defs []def
	defaultName := ""
	for _, p := range paths {
		data, err := os.ReadFile(p)
		if err != nil {
			return err
		}
		var d def
		if err := yaml.Unmarshal(data, &d); err != nil {
			return fmt.Errorf("%s: %w", p, err)
		}
		if d.Name == "" || d.Ident == "" {
			return fmt.Errorf("%s: name and ident are required", p)
		}
		for _, f := range fields {
			if d.Colors[f[0]] == "" {
				return fmt.Errorf("%s: missing color %q", p, f[0])
			}
		}
		if d.Default {
			if defaultName != "" {
				return fmt.Errorf("%s: more than one theme marked default", p)
			}
			defaultName = d.Name
		}
		defs = append(defs, d)
	}
	if defaultName == "" {
		return fmt.Errorf("no theme marked `default: true`")
	}

	var b bytes.Buffer
	fmt.Fprintln(&b, "// Code generated by theme/themes/gen; DO NOT EDIT.")
	fmt.Fprintln(&b, "// Source: theme/themes/defs/*.yaml")
	fmt.Fprintln(&b, "package themes")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, `import "github.com/atterpac/dado/theme"`)
	fmt.Fprintln(&b)

	for _, d := range defs {
		fmt.Fprintf(&b, "// %s is the %q theme.\n", d.Ident, d.Name)
		fmt.Fprintf(&b, "var %s = theme.MustFromColors(theme.ThemeColors{\n", d.Ident)
		for _, f := range fields {
			fmt.Fprintf(&b, "\t%s: theme.C(%q),\n", f[1], d.Colors[f[0]])
		}
		fmt.Fprintln(&b, "})")
		fmt.Fprintln(&b)
	}

	fmt.Fprintf(&b, "// DefaultName is the name of the default theme.\n")
	fmt.Fprintf(&b, "const DefaultName = %q\n\n", defaultName)

	fmt.Fprintln(&b, "// All returns all built-in themes keyed by name. Themes marked")
	fmt.Fprintln(&b, "// `hidden: true` are excluded; resolve those by name with Get.")
	fmt.Fprintln(&b, "func All() map[string]theme.Theme {")
	fmt.Fprintln(&b, "\treturn map[string]theme.Theme{")
	for _, d := range defs {
		if d.Hidden {
			continue
		}
		fmt.Fprintf(&b, "\t\t%q: %s,\n", d.Name, d.Ident)
	}
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)

	fmt.Fprintln(&b, "// all returns every built-in theme keyed by name, including hidden ones.")
	fmt.Fprintln(&b, "func all() map[string]theme.Theme {")
	fmt.Fprintln(&b, "\tm := All()")
	for _, d := range defs {
		if d.Hidden {
			fmt.Fprintf(&b, "\tm[%q] = %s\n", d.Name, d.Ident)
		}
	}
	fmt.Fprintln(&b, "\treturn m")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)

	names := make([]string, 0, len(defs))
	for _, d := range defs {
		if d.Hidden {
			continue
		}
		names = append(names, d.Name)
	}
	sort.Strings(names)
	fmt.Fprintln(&b, "// Names returns a sorted list of all built-in theme names, excluding")
	fmt.Fprintln(&b, "// themes marked `hidden: true`.")
	fmt.Fprintln(&b, "func Names() []string {")
	fmt.Fprintln(&b, "\treturn []string{")
	for _, n := range names {
		fmt.Fprintf(&b, "\t\t%q,\n", n)
	}
	fmt.Fprintln(&b, "\t}")
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b)

	fmt.Fprintln(&b, "// Get returns a theme by name, or nil if not found. Hidden themes are")
	fmt.Fprintln(&b, "// resolvable here even though they are absent from All and Names.")
	fmt.Fprintln(&b, "func Get(name string) theme.Theme { return all()[name] }")
	fmt.Fprintln(&b)
	fmt.Fprintln(&b, "// Default returns the default theme.")
	fmt.Fprintf(&b, "func Default() theme.Theme { return Get(DefaultName) }\n")

	src, err := format.Source(b.Bytes())
	if err != nil {
		return fmt.Errorf("format generated source: %w\n%s", err, b.String())
	}
	return os.WriteFile("themes_gen.go", src, 0o644)
}
